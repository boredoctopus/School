#include <iostream>

using namespace std;

int main(int argc, char* argv[])
{
  int daysInWeek = 7;
  int weeksInYear = 52;

  cout << "Days in week: " << daysInWeek << '\n' ;
  cout << "Weeks in year: " << weeksInYear << '\n' ;
  cout << "Days in year: " << daysInWeek * weeksInYear << '\n';
}
